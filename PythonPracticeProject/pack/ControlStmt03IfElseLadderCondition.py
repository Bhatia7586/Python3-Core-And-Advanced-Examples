#!/usr/bin/python

'''Syntax of else if ladder:
if expression1:
   statement(s)
elif expression2:
   statement(s)
elif expression3:
   statement(s)
else:
   statement(s)
'''
a = 1
if a == 5:
    print("If condition is satisfied")
    print(a)
elif a == 1:
    print("else if condition is satisfied")
    print(a)
else:
    print("else condition is satisfied")
    print(a)

