#!/usr/bin/python

a = 60            # 60 = 0011 1100 
b = 13            # 13 = 0000 1101 
print("Bitwise Operators a&b : ", a & b) # 12 = 0000 1100
print("Bitwise Operators a|b : ", a | b) # 61 = 0011 1101 
print("Bitwise Operators a^b : ", a ^ b) # 49 = 0011 0001
print("Bitwise Operators ~a : ", ~a) # -61 = 1100 0011
print("Bitwise Operators a<<2 : ", a << 2) # 240 = 1111 0000
print("Bitwise Operators a>>2 : ", a >> 2) # 15 = 0000 1111
